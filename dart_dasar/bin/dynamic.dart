void main() {
  // variable yang bisa di ubah tipe datanya
  dynamic variableContoh; // deklarasi
  variableContoh = 100; // int
  print(variableContoh);

  variableContoh = true; // ubah menjadi boolean
  print(variableContoh);

  variableContoh = 'Bahrul'; // ubah menjadi string
  print(variableContoh);

  dynamic variableContoh1 = 86; // deklarasi dan langsung isi
  print(variableContoh1); // int

  variableContoh1 = false;
  print(variableContoh1);

  variableContoh1 = 'BM';
  print(variableContoh1);
}

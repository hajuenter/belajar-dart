void main() {
  // && dan
  // || atau
  // ! kebalikan

  // && nilai akan true apabila 2 nilai sama sama true
  var nilai = 70;
  var hadir = 70;
  var cekNilai = nilai >= 50; // true
  var cekHadir = hadir >= 50; // true

  var lulus = cekNilai && cekHadir; // true dan true
  print(lulus); // true

  // || nilai akan true apabila salah 1 nilai adalah true,
  // jika dua duanya false maka false

  // ! kebalikan misal nilai true maka hasil false, dan sebaliknya
}
